<template>
  <div :class="classObj" class="app-wrapper">
    <div v-if="device==='mobile'&&sidebar.opened" class="drawer-bg" @click="handleClickOutside"/>
    <sidebar class="sidebar-container"/>
    <div class="main-container">
      <navbar/>
      <app-main/>
    </div>
  </div>
</template>

<script>
import { Navbar, Sidebar, AppMain } from './components'
import TagsView from '@/app/tags-view/components'
import ResizeMixin from './mixin/ResizeHandler'

export default {
  name: 'Layout',
  components: {
    Navbar,
    Sidebar,
    AppMain,
    TagsView
  },
  mixins: [ResizeMixin],
  computed: {
    sidebar() {
      return this.$store.state.layout.sidebar
    },
    device() {
      return this.$store.state.layout.device
    },
    classObj() {
      return {
        hideSidebar: !this.$store.state.layout.sidebar.opened,
        openSidebar: this.$store.state.layout.sidebar.opened,
        withoutAnimation: this.$store.state.layout.sidebar.withoutAnimation,
        mobile: this.$store.state.layout.device === 'mobile'
      }
    }
  },
  methods: {
    handleClickOutside() {
      this.$store.dispatch('closeSideBar', { withoutAnimation: false })
    }
  }
}
</script>

